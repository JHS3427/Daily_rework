import pandas as pd
import numpy as np

# 1. 데이터 생성 (일부러 비어있는 값 'None'을 넣습니다)
data = {
    '기기명': ['아이폰30', '갤럭시30', '아이폰300', None],
    '가격': [1000000, 3000000, None, 5000000]
}
df = pd.DataFrame(data)

# 2. 비어있는 값이 있는지 확인해 보세요 (isnull)
print("--- 결측치 확인 ---")
print(df.isnull())

print(df.dropna())
# 3. 가격이 비어있는 곳(None)을 0으로 채워보세요 (fillna)
df['가격'] = df['가격'].fillna(0)

# 4. 이름이 비어있는 행을 삭제해 보세요 (dropna)
df = df.dropna(subset=['기기명'])

print("\n--- 정제된 데이터 ---")
print(df)

# 테스트 데이터 생성
data = {
    '브랜드': ['삼성', '애플', '삼성', '애플', '샤오미'],
    '모델명': ['갤럭시 S24', '아이폰 15', '갤럭시 Z플립5', '아이폰 14', '미14'],
    '가격': [1200000, 1500000, 1100000, None, 800000],
    '재고': [10, 5, 0, 3, 20]
}
df = pd.DataFrame(data)

# --- 미션 시작 ---

# 1. '가격' 컬럼에 비어있는 값(None)을 데이터프레임 전체 가격의 '평균값'으로 채워주세요.
# 힌트: df['가격'].mean()을 사용하면 평균을 구할 수 있습니다. 

print("\n--- 문1  ---")
df['가격'] = df['가격'].fillna(df['가격'].mean())
print(df)

# 2. '재고'가 0개인 행(갤럭시 Z플립5)을 찾아서 데이터프레임에서 아예 삭제해 주세요.

print("\n--- 문2  ---")
new_df= df[df['재고']>0]
print(new_df)


# 3. 'loc'를 사용하여 '브랜드'가 '삼성'인 데이터의 '모델명'과 '가격'만 출력해 보세요. [cite: 68, 69]

print("\n--- 문3  ---")
print(df.loc[df['브랜드']=="삼성"].loc[:,'모델명':'가격'])
print(df.loc[df['브랜드']=="삼성",['모델명','가격']])


print("\n--- 최종 결과 ---")
print(df)
